from flask import Flask, render_template

# إنشاء كائن التطبيق - هذا السطر يخبر بايثون أننا نصمم موقع ويب
app = Flask(__name__)

# -------------------------------------------------------
# قسم المسارات (Routes): وظيفتها تحديد ماذا يظهر في كل رابط
# -------------------------------------------------------

# رابط الصفحة الرئيسية (عند فتح الموقع مباشرة)
@app.route('/')
def index():
    # render_template تعني: "اذهب لمجلد templates واعرض الملف التالي"
    return render_template('index.html')

# رابط صفحة الخدمات (عندما ينقر الزائر على "خدماتي")
@app.route('/services')
def services():
    return render_template('services.html')

# رابط صفحة اتصل بنا (عندما ينقر الزائر على "اتصل بي")
@app.route('/contact')
def contact():
    return render_template('contact.html')

# -------------------------------------------------------
# تشغيل السيرفر المحلي
# -------------------------------------------------------
if __name__ == '__main__':
    # debug=True تجعل الموقع يحدث نفسه تلقائياً عند تغيير الكود وتظهر الأخطاء بوضوح
    app.run(debug=True)